package com.example.javaknowledge.vehicle;

public class IndexPair {
    private int indexLow;
    private int indexHeight;

    public int getIndexLow() {
        return indexLow;
    }

    public int getIndexHeight() {
        return indexHeight;
    }

    public IndexPair(int l, int h) {
        this.indexLow = l;
        this.indexHeight = h;
    }
}
